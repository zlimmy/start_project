package com.huimodel.api.base;

/**
 * @author FZ
 */
public class AccountItem {

	private String created;
	private String pay_no;
	private String tid;
	private String flag;
	private String tag;
	private String paycode;
	private String pay_from;
	private String opposite_name;
	private double credit;
	private double charge;
	public String getCreated() {
		return created;
	}
	public void setCreated(String created) {
		this.created = created;
	}
	public String getPay_no() {
		return pay_no;
	}
	public void setPay_no(String pay_no) {
		this.pay_no = pay_no;
	}
	public String getTid() {
		return tid;
	}
	public void setTid(String tid) {
		this.tid = tid;
	}
	public String getFlag() {
		return flag;
	}
	public void setFlag(String flag) {
		this.flag = flag;
	}
	public String getTag() {
		return tag;
	}
	public void setTag(String tag) {
		this.tag = tag;
	}
	public String getPaycode() {
		return paycode;
	}
	public void setPaycode(String paycode) {
		this.paycode = paycode;
	}
	public String getPay_from() {
		return pay_from;
	}
	public void setPay_from(String pay_from) {
		this.pay_from = pay_from;
	}
	public String getOpposite_name() {
		return opposite_name;
	}
	public void setOpposite_name(String opposite_name) {
		this.opposite_name = opposite_name;
	}
	public double getCredit() {
		return credit;
	}
	public void setCredit(double credit) {
		this.credit = credit;
	}
	public double getCharge() {
		return charge;
	}
	public void setCharge(double charge) {
		this.charge = charge;
	}
	
	
	
}
